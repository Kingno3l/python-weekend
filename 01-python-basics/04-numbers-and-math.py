# 04-numbers-and-math.py
# This script demonstrates how to work with numbers and perform math operations.

# Example 1: Basic math
print(10 + 5)  # Output: 15
print(10 - 5)  # Output: 5
print(10 * 5)  # Output: 50
print(10 / 5)  # Output: 2.0

# Example 2: Modulus (remainder)
print(10 % 3)  # Output: 1 (remainder of 10 divided by 3)

# Example 3: Order of operations
print(2 + 3 * 4)  # Output: 14 (multiplication happens first)