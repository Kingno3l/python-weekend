# 01-lists.py
# This script demonstrates how to work with lists in Python.

# Example 1: Creating a list
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits)  # Output: Fruits: ['apple', 'banana', 'cherry']

# Example 2: Accessing list items
print("First fruit:", fruits[0])  # Output: First fruit: apple

# Example 3: Modifying a list
fruits.append("orange")
print("Updated Fruits:", fruits)  # Output: Updated Fruits: ['apple', 'banana', 'cherry', 'orange']